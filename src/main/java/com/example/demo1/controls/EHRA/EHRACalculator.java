package com.example.demo1.controls.EHRA;

public class EHRACalculator {

    /**
     * Оценка выраженности симптомов фибрилляции предсердий
     * по шкале EHRA (European Heart Rhythm Association Symptom Scale)
     *
     * Шкала EHRA используется для оценки влияния симптомов
     * фибрилляции предсердий на повседневную активность пациента.
     * Она позволяет стандартизировать клиническую оценку тяжести
     * симптомов и помогает при выборе стратегии лечения.
     *
     * Модифицированная версия шкалы (mEHRA) дополнительно разделяет
     * класс II на две подкатегории (IIa и IIb), что позволяет
     * точнее учитывать субъективное восприятие симптомов пациентом.
     *
     * Интерпретация классов EHRA:
     *
     * I — Симптомы отсутствуют.
     *
     * II — Легкая симптоматика, обычная повседневная активность
     *      не нарушена.
     *
     * III — Выраженная симптоматика,
     *       обычная повседневная активность затруднена.
     *
     * IV — Инвалидизирующая симптоматика,
     *      обычная повседневная активность невозможна.
     *
     * Интерпретация классов mEHRA:
     *
     * I — Симптомы отсутствуют.
     *
     * IIa — Легкая симптоматика,
     *       симптомы не влияют на нормальную повседневную активность
     *       и не вызывают беспокойства у пациента.
     *
     * IIb — Умеренная симптоматика,
     *       симптомы не влияют на повседневную активность,
     *       но вызывают субъективное беспокойство у пациента.
     *
     * III — Тяжелая симптоматика,
     *       обычная повседневная активность затруднена.
     *
     * IV — Инвалидизирующая симптоматика,
     *      нормальная повседневная активность невозможна.
     *
     * Примечания:
     * - Шкала применяется при диагностике и наблюдении пациентов
     *   с фибрилляцией предсердий.
     * - Используется для оценки эффективности лечения
     *   и сравнения результатов клинических исследований.
     */

    public static EHRAResult calc(String status) {

        if (status == null) {
            return new EHRAResult("-", "Выберите состояние пациента");
        }

        switch (status) {

            case "Симптомов нет":
                return new EHRAResult("EHRA I",
                        "Симптомы отсутствуют");

            case "Легкая симптоматика, обычная повседневная активность не нарушена":
                return new EHRAResult("EHRA II",
                        "Легкие симптомы, повседневная активность не ограничена");

            case "Тяжелая симптоматика, обычная повседневная активность затруднена":
                return new EHRAResult("EHRA III",
                        "Выраженные симптомы, повседневная активность ограничена");

            case "Инвалидизирующая симптоматика, обычная повседневная активность невозможна":
                return new EHRAResult("EHRA IV",
                        "Инвалидизирующие симптомы");

            case "mEHRA 1 — симптомов нет":
                return new EHRAResult("mEHRA I",
                        "Симптомы отсутствуют");

            case "mEHRA 2a — лёгкая симптоматика, симптомы не влияют на активность и не вызывают беспокойства":
                return new EHRAResult("mEHRA IIa",
                        "Легкие симптомы без беспокойства пациента");

            case "mEHRA 2b — умеренная симптоматика, симптомы не влияют на активность, но вызывают беспокойство":
                return new EHRAResult("mEHRA IIb",
                        "Умеренные симптомы с субъективным дискомфортом");

            case "mEHRA 3 — тяжёлая симптоматика, обычная повседневная активность затруднена":
                return new EHRAResult("mEHRA III",
                        "Выраженные симптомы, активность ограничена");

            case "mEHRA 4 — инвалидизирующая симптоматика, нормальная повседневная активность невозможна":
                return new EHRAResult("mEHRA IV",
                        "Инвалидизирующие симптомы");

            default:
                return new EHRAResult("-", "Нет данных");
        }
    }
}