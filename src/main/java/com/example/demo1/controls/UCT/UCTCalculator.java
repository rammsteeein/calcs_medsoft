package com.example.demo1.controls.UCT;

/**
 * Тест контроля крапивницы (UCT — Urticaria Control Test)
 *
 * Используется для оценки контроля индуцированной
 * и хронической крапивницы за последние 4 недели.
 *
 * Каждый вопрос оценивается от 0 до 4 баллов.
 *
 * Разделы:
 *
 * 1. Насколько сильно беспокоили проявления крапивницы:
 * - Очень сильно — 0
 * - Сильно — 1
 * - Достаточно — 2
 * - Немного — 3
 * - Не беспокоили — 4
 *
 * 2. Насколько крапивница ухудшила качество жизни:
 * - Очень сильно — 0
 * - Сильно — 1
 * - Достаточно — 2
 * - Немного — 3
 * - Не ухудшила — 4
 *
 * 3. Как часто лечение было недостаточным:
 * - Очень часто — 0
 * - Часто — 1
 * - Иногда — 2
 * - Редко — 3
 * - Ни разу — 4
 *
 * 4. Насколько успешно удавалось контролировать крапивницу:
 * - Не удавалось — 0
 * - Немного — 1
 * - Достаточно — 2
 * - Хорошо — 3
 * - Очень хорошо — 4
 *
 * Максимальный балл: 16
 *
 * Интерпретация:
 * 12–16 — контролируемое течение крапивницы
 * ≤11 — неконтролируемое течение крапивницы
 */

public class UCTCalculator {

    public static UCTResult calc(
            Integer q1,
            Integer q2,
            Integer q3,
            Integer q4
    ) {

        int total = 0;

        if (q1 != null) total += q1;
        if (q2 != null) total += q2;
        if (q3 != null) total += q3;
        if (q4 != null) total += q4;

        return new UCTResult(
                total,
                interpret(total)
        );
    }

    private static String interpret(int score) {

        if (score >= 12) {
            return "Контролируемое течение крапивницы";
        }

        return "Неконтролируемое течение крапивницы";
    }
}