package com.example.demo1.controls.HoehnYahr;

public class HoehnYahrCalculator {
    /**
     * Расчет стадии болезни Паркинсона по шкале Хен-Яра
     *
     * Шкала Хен и Яра используется для оценки степени двигательных нарушений
     * при болезни Паркинсона и определения стадии прогрессирования заболевания.
     * Она позволяет стратифицировать пациентов по выраженности двигательной
     * дисфункции и ее влиянию на повседневную активность.
     *
     *
     * Интерпретация стадий:
     *
     * 0 — Симптомы отсутствуют.
     *
     * 1 — Односторонние двигательные нарушения
     *     (тремор, ригидность или брадикинезия),
     *     практически не влияющие на самообслуживание и трудовую деятельность.
     *
     * 2 — Двусторонняя симптоматика без нарушения равновесия.
     *     Возможны тремор и ригидность умеренной интенсивности,
     *     создающие незначительные трудности при выполнении повседневных задач.
     *
     * 3 — Легкая или умеренная симптоматика с постуральной неустойчивостью.
     *     Пациент нуждается в помощи для восстановления равновесия
     *     при толчковом тесте, но физическая независимость сохранена.
     *
     * 4 — Тяжелая симптоматика.
     *     Пациент способен стоять и ходить без поддержки,
     *     но выраженные двигательные нарушения существенно ограничивают активность.
     *
     * 5 — Тяжелая инвалидизация.
     *     Пациент без посторонней помощи прикован к инвалидному креслу или постели.
     *
     * Примечания:
     * - Шкала широко применяется в клинической практике и научных исследованиях.
     * - Используется для оценки прогрессирования заболевания
     *   и эффективности терапии при болезни Паркинсона.
     */

    public static HoehnYahrResult calc(String status) {

        if (status == null) {
            return new HoehnYahrResult(0, "Выберите состояние пациента");
        }

        int stage = mapStatusToStage(status);
        String interpretation = interpret(stage);

        return new HoehnYahrResult(stage, interpretation);
    }

    private static int mapStatusToStage(String status) {

        switch (status) {

            case "Симптомы отсутствуют":
                return 0;

            case "Односторонние двигательные нарушения, практически не мешающие самообслуживанию":
                return 1;

            case "Двусторонняя симптоматика без нарушения равновесия":
                return 2;

            case "Легкая или умеренная симптоматика, есть постуральная неустойчивость":
                return 3;

            case "Тяжелая симптоматика, но способен ходить и стоять без поддержки":
                return 4;

            case "Без посторонней помощи прикован к инвалидному креслу или постели":
                return 5;

            default:
                return 0;
        }
    }

    private static String interpret(int stage) {

        switch (stage) {

            case 0:
                return "Симптомы отсутствуют";

            case 1:
                return "Односторонние двигательные нарушения, повседневная активность сохранена";

            case 2:
                return "Двусторонние симптомы без нарушения равновесия";

            case 3:
                return "Постуральная неустойчивость, физическая независимость сохранена";

            case 4:
                return "Тяжелая инвалидизация, но способен стоять и ходить";

            case 5:
                return "Тяжелая инвалидизация, пациент прикован к креслу или постели";

            default:
                return "Нет данных";
        }
    }
}