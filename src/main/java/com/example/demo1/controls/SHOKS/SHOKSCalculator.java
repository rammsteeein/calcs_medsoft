package com.example.demo1.controls.SHOKS;

public class SHOKSCalculator {

    /**
     * Расчет баллов по шкале ШОКС (модификация Мареева В.Ю., 2000)
     *
     * Симптомы и признаки с оценкой:
     * 1. Одышка:
     *    - 0: нет
     *    - 1: при нагрузке
     *    - 2: в покое
     * 2. Изменение веса за последнюю неделю:
     *    - 0: нет
     *    - 1: увеличился
     * 3. Жалобы на перебои в работе сердца:
     *    - 0: нет
     *    - 1: есть
     * 4. Положение в постели:
     *    - 0: горизонтально
     *    - 1: с приподнятым головным концом (две и более подушек)
     *    - 2: плюс просыпается от удушья
     *    - 3: сидя
     * 5. Набухшие шейные вены:
     *    - 0: нет
     *    - 1: лежа
     *    - 2: стоя
     * 6. Хрипы в легких:
     *    - 0: нет
     *    - 1: нижние отделы
     *    - 2: до лопаток
     *    - 3: над всей поверхностью легких
     * 7. Наличие ритма галопа:
     *    - 0: нет
     *    - 1: есть
     * 8. Печень:
     *    - 0: не увеличена
     *    - 1: до 5 см
     *    - 2: более 5 см
     * 9. Отеки:
     *    - 0: нет
     *    - 1: пастозность
     *    - 2: отеки
     *    - 3: анасарка
     * 10. САД (систолическое артериальное давление):
     *    - 0: >120 мм рт. ст.
     *    - 1: 100-120 мм рт. ст.
     *    - 2: <100 мм рт. ст.
     *
     * Интерпретация итогового балла (сумма баллов всех симптомов):
     * - 0 баллов: отсутствие клинических признаков СН
     * - I ФК: <= 3 баллов
     * - II ФК: 4–6 баллов
     * - III ФК: 7–9 баллов
     * - IV ФК: >9 баллов
     *
     * Примечания:
     * - Каждому признаку присваивается балл в зависимости от выраженности.
     * - Итоговый балл суммируется для определения функционального класса сердечной недостаточности.
     * - Используется для клинической оценки состояния пациента с хронической сердечной недостаточностью.
     */

    public static SHOKSResult calc(
            int odyshka, int ves, int pereboi, int polozhenie,
            int sheinyeVeny, int hripy, int galop, int pechen,
            int oteki, int SAD) {

        int score;

        score = odyshka + ves + pereboi + polozhenie + sheinyeVeny +
                hripy + galop + pechen + oteki + SAD;

        String interpretation;
        if (score <= 3) interpretation = "I ФК";
        else if (score <= 6) interpretation = "II ФК";
        else if (score <= 9) interpretation = "III ФК";
        else interpretation = "IV ФК";

        return new SHOKSResult(interpretation, score);
    }
}
